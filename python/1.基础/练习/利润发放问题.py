#!/usr/bin/python3.7
#Filename.利润发放问题.py

#I<10万，提10%；10<I<20，超出部分提7.5%；依此类推...

I = int(input('请输入当月利润（元）I='))  #必须要用int整形，否则下方if语句中无法进行比较
a = [100,60,40,20,10,0]
b = [0.01,0.015,0.03,0.05,0.075,0.1]    #1%、1.5%、3%、5%、7.5%、10%
m = 0
n = 0

for i in range(0,6):
    c = a[i]*10**4
    if I > c:
        m = (I - c)*b[i]  #超过a[i]的区间利润
        n = n + m
        I = c

print('总提成是：%d元'%n)
        
