#!/usr/bin/python
# Filename：函数功能展示暨定义函数.py

def h():
    print('Hello World')

h()         #要运行所定义的函数，必须调用它


#def用来定义函数，即定义h()为一个函数，括号内可以包含一些变量，该行以:结尾。
#定义函数后，下面的一块语句就是所定义的函数内容，即函数体。
#h是所定义的函数用来命名的 标识符 名称，以便在程序的任何地方调用这个函数。


#在数学中，用字母y来定义一个函数，即y是所定义的函数的标识符。
#y=x+1，x即是变量或者说参数，这个式子就是所定义的函数内容，即函数体。
#同样，在一个数学题中，y可以随便调用。
